    <details>
        <summary>1</summary>
        <p style="border:2px solid green;">Requiring employees to use unique, strong passwords for their accounts and to
            change them regularly.</p>
    </details>
    <details>
        <summary>2</summary>
        <p style="border:2px solid red;">Prohibiting the sharing of passwords, except in situations where it is
            absolutely necessary.</p>
    </details>
    <details>
        <summary>3</summary>
        <p style="border:2px solid purple;">Providing employees with access to password management tools to help them
            securely store and manage their passwords.</p>
    </details>
    <details>
        <summary>4</summary>
        <p style="border:2px solid orange;">Establishing procedures for revoking access to shared credentials when an
            employee leaves the company or their role changes.</p>
    </details>
    <details>
        <summary>5</summary>
        <p style="border:2px solid yellow;">Regularly auditing access to shared credentials and monitoring for unusual
            Internet activity.</p>
    </details>
