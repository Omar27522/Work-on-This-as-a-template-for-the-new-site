    <summary class="active">Credentials</summary>
    <p tabindex="0" class="selection">In a company, sharing credentials among employees can be a risky practice, as it can lead to security
        vulnerabilities and compromise the confidentiality and integrity of sensitive information. It is generally best
        to avoid sharing credentials whenever possible.If it is necessary for employees to share credentials, it is
        important to have a clear and secure policy in place to ensure that the practice is carried out safely. Some key
        considerations for a company policy on credential sharing might include:</p>
        <br />
    <figure>
        <figcaption>
            Share!&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Securely,
