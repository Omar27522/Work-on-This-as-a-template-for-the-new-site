    <h2>Humans have senses to measure the environment they are in.</h2>
    <p>One of these senses is <b><u>sight</u></b>. When light bounces off an object, human eyes detect the variations of
        these light bounces; then our brain makes the necessary calculations to represent it in the natural world,
        A.K.A. reality. In other words, if the object is there and you are seeing it, then you can test the reality of
        said object by measuring it with other senses.</p>

    <h3>The Evolution of Computer Graphics</h3>
    <p>The history of computer graphics dates back to the 1950s, beginning with the Whirlwind computer at MIT which used a CRT display for output. In 1963, Ivan Sutherland revolutionized the field with Sketchpad, the first computer-aided design program. The 1970s saw the birth of 2D computer graphics in commercial applications, while the 1980s brought us breakthrough developments in 3D graphics and animation. The 1990s witnessed the rise of dedicated graphics processing units (GPUs), leading to today's photorealistic rendering capabilities and real-time 3D graphics.</p>

    <h3>2D Graphics: The Foundation</h3>
    <p>2D graphics form the basis of digital imaging, consisting of pixels arranged in a two-dimensional grid. These graphics include everything from basic user interfaces to digital art and photographs. Vector graphics, another form of 2D imaging, use mathematical equations to create scalable images that maintain quality at any size. Common applications include logo design, typography, and technical illustrations.</p>

    <h3>3D Graphics: Adding Depth</h3>
    <p>3D graphics introduce the z-axis, creating virtual objects with depth, volume, and perspective. Modern 3D graphics utilize complex techniques like:
        <ul>
            <li>Polygon mesh modeling for creating object shapes</li>
            <li>Texture mapping for adding surface details</li>
            <li>Ray tracing for realistic lighting and shadows</li>
            <li>Real-time rendering for interactive experiences</li>
        </ul>
    These technologies power everything from video games to architectural visualization and scientific simulations.</p>

    <h3>Digital Photography and Imaging Hardware</h3>
    <p>Digital photography represents a crucial aspect of modern imaging technology. The key components include:</p>
    
    <h4>Image Sensors</h4>
    <p>At the heart of digital cameras are two main types of image sensors:
        <ul>
            <li><strong>CCD (Charge-Coupled Device):</strong> Known for high-quality images with less noise, traditionally used in professional cameras</li>
            <li><strong>CMOS (Complementary Metal-Oxide Semiconductor):</strong> More energy-efficient and cheaper to manufacture, now the dominant technology in most modern cameras</li>
        </ul>
    These sensors convert light into electrical signals through millions of photosites (pixels).</p>

    <h4>Key Photography Components</h4>
    <p>Several elements work together to create digital images:
        <ul>
            <li><strong>Lens System:</strong> Controls focus, zoom, and light gathering through adjustable apertures</li>
            <li><strong>Image Processor:</strong> Converts sensor data into digital images, handling tasks like noise reduction and color processing</li>
            <li><strong>Resolution:</strong> Measured in megapixels, determining the detail level of captured images</li>
            <li><strong>Dynamic Range:</strong> The camera's ability to capture both bright and dark areas in a single shot</li>
        </ul>
    </p>

    <h4>Digital Image Processing</h4>
    <p>After capture, images go through several processing steps:
        <ul>
            <li><strong>RAW Processing:</strong> Converting raw sensor data into viewable images</li>
            <li><strong>Color Correction:</strong> Adjusting white balance and color accuracy</li>
            <li><strong>Noise Reduction:</strong> Removing digital artifacts and sensor noise</li>
            <li><strong>Compression:</strong> Optimizing file size while maintaining image quality</li>
        </ul>
    </p>

    <h3>Modern Display Technologies</h3>
    <p>Today's display technology has evolved dramatically, offering unprecedented visual experiences:
        <ul>
            <li><strong>High Refresh Rates:</strong> Modern displays can achieve 144Hz, 240Hz, or even higher, providing smoother motion and reducing motion blur</li>
            <li><strong>Resolution:</strong> From 4K (3840x2160) to 8K (7680x4320) displays, offering incredibly detailed images</li>
            <li><strong>HDR (High Dynamic Range):</strong> Enabling a wider range of colors and brightness levels</li>
            <li><strong>OLED and Mini-LED:</strong> Delivering deeper blacks and better contrast ratios</li>
        </ul>
    </p>

    <h3>Virtual and Augmented Reality</h3>
    <p>The latest frontier in computer graphics involves immersive technologies:
        <ul>
            <li><strong>Virtual Reality (VR):</strong> Creates fully immersive 3D environments, using high-resolution displays, precise motion tracking, and sophisticated controllers for interaction</li>
            <li><strong>Augmented Reality (AR):</strong> Overlays digital content onto the real world, using cameras and sensors to blend virtual and physical spaces</li>
            <li><strong>Mixed Reality (MR):</strong> Combines elements of both VR and AR, allowing virtual objects to interact with the physical environment</li>
        </ul>
    These technologies are revolutionizing fields from gaming and entertainment to education, healthcare, and industrial design.</p>

    <h3>Modern Computer Imaging</h3>
