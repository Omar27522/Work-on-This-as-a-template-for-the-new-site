    <p tabindex="0" class="selection">From the dawn of human civilization, the need to communicate has driven countless
        innovations. As societies
        evolved, so did their methods of communication, transitioning from smoke signals and carrier pigeons to
        telegraphs and telephones. With the advent of the internet in the late 20th century, a new era of instantaneous,
        global communication was born.<br />
        This technological revolution transformed how people connect, making it possible
        to share information and stay in touch with others regardless of geographical boundaries. The following sections
        delve into the modern landscape of internet communication, highlighting the rise of instant messaging
        applications and their profound impact on our daily lives.
    </p>
    <p tabindex="0" class="selection">Aside from email, we can communicate instantaneously over the internet using a
        messaging program (APP), now days we
        can send attached video files, pictures, links and attachments over these apps. Most modern messaging
        applications
        offer private group chats, video/voice calling the person(s), make and accept payments, and of course, play
        games.
    </p>
    <p tabindex="0" class="selection">“Though the term dates from the 1990s, instant messaging predates the Internet,
        first appearing on multi-user
        operating systems like Compatible Time-Sharing System (CTSS) and Multiplexed Information and Computing Service
        (Multics)[12][13] in the mid-1960s. Initially, some of these systems were used as notification systems for
        services
        like printing, but quickly were used to facilitate communication with other users logged into the same machine.
        CTSS
        facilitated communication via text message for up to 30 people.[14] “ <a
            href="https://en.wikipedia.org/wiki/Instant_messaging">Wikipedia</a>
    </p>
