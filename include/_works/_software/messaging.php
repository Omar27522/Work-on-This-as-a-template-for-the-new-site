<h1 id="title">Messaging</h1>

<p tabindex="0" class="selection">Aside from email, we can communicate instantaneously over the internet using a messaging program (APP), now days we
    can send attached video files, pictures, links and attachments over these apps. Most modern messaging applications
    offer private group chats, video/voice calling the person(s), make and accept payments, and of course, play games.
</p>
<p tabindex="0" class="selection">“Though the term dates from the 1990s, instant messaging predates the Internet, first appearing on multi-user
    operating systems like Compatible Time-Sharing System (CTSS) and Multiplexed Information and Computing Service
    (Multics)[12][13] in the mid-1960s. Initially, some of these systems were used as notification systems for services
    like printing, but quickly were used to facilitate communication with other users logged into the same machine. CTSS
    facilitated communication via text message for up to 30 people.[14] “ <a
