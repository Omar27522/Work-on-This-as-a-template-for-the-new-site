<h1 id="title">File compression</h1>
<div class="blurb">
<p tabindex="0" class="selection">The process of reducing the size of a file in order to save space or make it easier
to transmit over a network. The history of file compression can be traced back to the early days of computing, when data
was often stored on tapes or punched cards.</p>
<p tabindex="0" class="selection">One of the earliest methods of file compression was run-length encoding, which was used in the 1960s to compress data
    stored on magnetic tapes. This method involved replacing consecutive occurrences of the same data value with a
    single value and a count of the number of occurrences.</p>
<p tabindex="0" class="selection">In the 1980s, the development of personal computers and the need to transmit files over networks led to the
    development of more sophisticated file compression algorithms. One of the most widely used algorithms is called the
    Lempel-Ziv-Welch (LZW) algorithm, which is used in the popular GIF image format and the ZIP file format.</p>
<p tabindex="0" class="selection">In the 1990s, the growth of the internet and the need to transmit large files, such as multimedia content, led to the
    development of even more advanced file compression algorithms, including the MP3 audio format and the JPEG image
    format.</p>
<p style="text-indent: 27px;" tabindex="0" class="selection">Today, file compression is an essential part of modern computing, and a wide variety of
    algorithms and tools are available for compressing different types of data.<br />

<details>
    <summary tabindex="0" class="selection">ZIP:</summary><fieldset tabindex="0" class="selection">
        <p>This is a widely used file format for compression and archiving. It can support compression
    of multiple files into a single archive, and it can also support password-protected archives.</p></fieldset>
</details>

<details>
    <summary tabindex="0" class="selection">RAR:</summary><fieldset tabindex="0" class="selection">
        <p>This is another popular file format for compression and archiving, developed by WinRAR.
    It supports a variety of advanced features, including the ability to split an archive into multiple smaller files for easier storage or transfer.</p></fieldset>
</details>

<details>
    <summary tabindex="0" class="selection">TAR:</summary><fieldset tabindex="0" class="selection">
        <p>This file format is used to create archive files, typically called "tarballs."
    It is often used in conjunction with a compression algorithm, such as gzip or bzip2, to create a compressed archive file.</p></fieldset>
</details>

<details>
    <summary tabindex="0" class="selection">GZ:</summary><fieldset tabindex="0" class="selection">
        <p>This file format is based on the DEFLATE algorithm, which is a popular data compression method.
    It is often used to compress single files or directories, and the resulting file has a .gz extension.</p></fieldset>
</details>

<details>
    <summary tabindex="0" class="selection">BZ2:</summary><fieldset tabindex="0" class="selection">
        <p>This file format is based on the bzip2 algorithm, which is another popular data compression method.
    It is often used to compress single files or directories, and the resulting file has a .bz2 extension.</p></fieldset>
</details>

</p>
