        <summary class="card2">Computer repair</summary>
        <p>There are many different components that can go wrong and it can be difficult to diagnose the issue.<br />
            Fortunately, there are some basic steps you can take to troubleshoot your computer and get it back up and
            running quickly.</p>
        <div tabindex="0" class="selection">
            <details tabindex="0" class="selection">
                <summary class="selection card2" style="width: 50%;">First, make sure all of your cables are securely
                    connected.</summary>
                <p><span tabindex="0" class="selection_2">Check the power cord, monitor cable, keyboard cable, mouse
                        cable, and any other cables that may be connected to your computer.</span><br />
                    <span tabindex="0" class="selection_2">If any of these are loose or disconnected, reconnect them
                        securely.</span>
                </p>
            </details>
            <details tabindex="0" class="selection">
                <summary class="selection card2" style="width: 50%;">Next, check your computer's power supply.</summary>
                <p>
                    <span tabindex="0" class="selection_2">Make sure the power switch is in the ON position and that the
                        power cord is plugged in securely.</span><br />
                    <span tabindex="0" class="selection_2">If the power switch is not in the "on" position, turn it on
                        and see if the computer powers up.</span><br />
                    <span tabindex="0" class="selection_2">If it does not, check to make sure the power cord is plugged
                        into a working outlet.</span>
                </p>
            </details>
            <details tabindex="0" class="selection">
                <summary class="selection card2" style="width: 50%;">Check your computer's hardware components.
                </summary>
                <p>
                    <span tabindex="0" class="selection_2">Make sure all of the fans are spinning correctly and that
                        there are no loose parts or wires inside of the case.</span><br />
                    <span tabindex="0" class="selection_2">Also check to make sure all of your RAM modules are seated
                        properly in their slots.</span>
                </p>
            </details>
        </div>
        <p>If all of these steps have been completed and your computer still does not turn on, it may be time to contact
            a <a href="https://www.google.com/search?q=computer+repair+technician+near+me"
                title="Google search: Computer Repair Technician Near Me">professional computer repair technician</a>
            for help.<br />
            They will be able to diagnose the issue more accurately and provide you with a solution quickly.</p>
    </details>

